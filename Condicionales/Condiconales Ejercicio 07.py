#Se desea escribir un programa que pida la altura de una persona
#si la altura es menor o igual a 150 cm Escribir mensaje «persona de
#baja estatura» si está entre 151cm y 170cm escribir mensaje
#«persona de altura media» y si la altura es mayor 171cm escribir
#mensaje «persona alta».

alt=float(input("Ingrese su altura (cm): "))
if alt<=150:
    print("Persona de baja estatura.")
elif alt>=151 and alt<=170:
    print("Persona de estatura media")
elif alt>=171:
    print("Persona de estatura alta.")




















    
