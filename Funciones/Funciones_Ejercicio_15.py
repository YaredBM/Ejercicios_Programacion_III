#Leer una frase y contar el numero de vocales (de cada una) que aparezcan
frase=input("Ingrese una frase: ")
voca1=frase.count("a")
voca2=frase.count("e")
voca3=frase.count("i")
voca4=frase.count("o")
voca5=frase.count("u")
print("a= ",voca1)
print("e= ",voca2)
print("i= ",voca3)
print("o= ",voca4)
print("u= ",voca5)
